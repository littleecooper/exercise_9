# The first_name is the variable name and the '=" assigns the object ('Eleanor') to that variable
first_name = "Eleanor"
last_name = "Cooper"
# The print function writes to the standard output stream
print(first_name, last_name)

# this is a list - indicated through the square brackets
name_list = ["Eleanor", "Cooper"]
# We have written this in to confirm the class type is a list by using the type function
print(type(name_list))

# Dictionaries use wiggly brackets and store data which is a key value pair (first_name : "First")
names_dictionary = {first_name: "First", last_name: "Last"}
print(names_dictionary)
print(type(names_dictionary))